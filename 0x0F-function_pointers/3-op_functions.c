/**
 * op_add -function to add two numbers
 * @a: the first integer
 * @b: the second integer
 * Return: The sum of the nubers.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- function substarct two numbers
 * @a: the first integer
 * @b: the second integer
 * Return: The difference of the nubers.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul- function to multiply two numbers
 * @a: the first integer
 * @b: the second integer
 * Return: The product of the nubers.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div- function divide two numbers
 * @a: the first integer
 * @b: the second integer
 * Return: The quotient of the nubers.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- function to get the modulud of two numbers
 * @a: the first integer
 * @b: the second integer
 * Return: The difference of the numbers.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
